package day2;

public class LoopDemo {
	
	    public static void main(String[] args) {
	        
	        // Loop from 1 to 10
	        for (int i = 1; i <= 10; i++) {
	            
	            // Check if number is even or odd
	            if (i % 2 == 0) {
	                System.out.println(i + " is Even");
	            } else {
	                System.out.println(i + " is Odd");
	            }
	        }
	    }
}


